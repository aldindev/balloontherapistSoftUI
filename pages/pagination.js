// Array of page contents
let contentData = [page1Content, page2Content];

function loadPage(pageNumber) {
  const contentContainer = document.getElementById("content-container");
  contentContainer.innerHTML = ""; // Clear existing content

  const pageContent = contentData[pageNumber - 1];
  contentContainer.innerHTML = pageContent; // Set new content

  // Update pagination links (active class)
  const paginationLinks = document.querySelectorAll(".pagination .page-item");
  paginationLinks.forEach((link) => link.classList.remove("active"));
  paginationLinks[pageNumber].classList.add("active");
}

// Load the first page by default
document.addEventListener("DOMContentLoaded", function () {
  loadPage(1);
});
